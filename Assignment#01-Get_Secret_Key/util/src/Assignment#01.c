void invSubBytes(unsigned int *inv_buf, int count){
	int i = 0;
	while(i < count){
		for(int j = 0; j < 16; j++){
		    if((inv_buf[i] >> 4) == j){
		       for(int k = 0; k < 16; k++){
		           if((inv_buf[i] & 0x0f) == k){
			       inv_buf[i] = invSbox[j][k];
			       break;
		           }
		        }
			break;
		    }
	    }
	    i++;
	   }	 
}

